# This program asks the user for their name and greets them with their name only if their names are Alice or Bob
# Author: Ross Downey

name = input ("Enter your name please:")

if name == 'Alice':
    print ('Hello Alice!')

elif name == 'Bob':
    print ('Hello Bob!')
    
else: 
    print ("Only Alice or Bob can be greeted, apologies!")